    private void tableProdutosCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProdutosCadastradosMouseClicked
        if (evt.getClickCount() >= 2) {
            int linha = tableProdutosCadastrados.getSelectedRow();

            ProdutoCombo produtoCombo = produtosCadastrados.get(linha);

            int resposta = JOptionPane.showConfirmDialog(this, "Remover produto do combo?", "Remover Produto", JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_OPTION) {
                try {
                    produtoComboDAO.excluir(produtoCombo);
                } catch (SQLException ex) {
                    Logger.getLogger(ComboDetalheView.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(this, "Produto removido do combo!", "Produto Removido", JOptionPane.INFORMATION_MESSAGE);
                carregarRegistrosTabela();
            }

        }
    }//GEN-LAST:event_tableProdutosCadastradosMouseClicked

    private void carregarRegistrosTabela() {
        try {
            produtosCadastrados = produtoComboDAO.listarTodosProdutosPorCombo(combo);
        } catch (SQLException ex) {
            Logger.getLogger(ProdutoView.class.getName()).log(Level.SEVERE, null, ex);
        }

        tableModelProdutoCombo = new ProdutoComboTableModel(produtosCadastrados);
        tableProdutosCadastrados.setModel(tableModelProdutoCombo);
    }

    private void carregarRegistrosCombo() {
        try {
            produtos = produtoDAO.listarTodos(0);
        } catch (SQLException ex) {
            Logger.getLogger(ProdutoView.class.getName()).log(Level.SEVERE, null, ex);
        }

        for (Produto p : produtos) {
            comboProdutos.addItem(p.getNome());
        }

    }

    private void reiniciarFormulario() {
        comboProdutos.setSelectedIndex(0);
        textFieldQuantidade.setText("");
    }

