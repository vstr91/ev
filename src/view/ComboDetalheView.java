    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        Produto produto = produtos.get(comboProdutos.getSelectedIndex());

        int quantidade = Integer.valueOf(textFieldQuantidade.getText().trim());

        if (quantidade > 0) {
            ProdutoCombo produtoCombo = new ProdutoCombo();
            produtoCombo.setProduto(produto);
            produtoCombo.setCombo(combo);
            produtoCombo.setQuantidade(quantidade);

            try {
                produtoComboDAO.editar(produtoCombo);
            } catch (SQLException ex) {
                Logger.getLogger(ComboDetalheView.class.getName()).log(Level.SEVERE, null, ex);
            }

            carregarRegistrosTabela();
            reiniciarFormulario();

        }


    }//GEN-LAST:event_btnAdicionarActionPerformed

